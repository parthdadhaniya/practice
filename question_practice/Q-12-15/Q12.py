# Q12. Write a Python program that takes two numbers as input and performs the following operations: addition, subtraction, multiplication, division, and modulus. Display the results.


number_1 = int(input("Enter a number 1 : "))
number_2 = int(input("Enter a number 2 : "))

addition = number_1 + number_2
print("addition: ", addition)

subtraction = number_1 - number_2
print("subtraction: ", subtraction)

multiplication = number_1 * number_2
print("multiplication: ", multiplication)

division = number_1 / number_2
print("division: ", division)

modulus = number_1 % number_2
print("modulus: ", modulus)
