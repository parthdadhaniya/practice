# Q7. Write a Python program to calculate the average of three numbers entered by the user.


number_1 = int(input("Enter a number_1 :"))
number_2 = int(input("Enter a number_2 :"))
number_3 = int(input("Enter a number_3 :"))

average = (number_1 + number_2 + number_3) / 3

print("Your Average number is : ", average)
